package com.primjer03.petlje_k_toka;

public class IfElsePrimjer {

    public static void main(String[] args) {
        int n = 19;
        // Provjera da li je n paran
        if (n % 2 == 0) {
            System.out.println("Broj " + n + " je paran!");
        } else {
            System.out.println("Broj " + n + " je neparan!");
        }
    }

}
